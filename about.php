                 <p> Bayesla State, being one of the nine oil producing states in the country has seemingly enjoyed
enormous gains from revenues accruing from the monthly allocations from the Federal
Government. To that extent, the state has benignly steered off route, robust and disparate
economic activities that would have otherwise taken us to greater heights in terms of job/wealth
creation, food security and standard of living to mention a few.</p>
<p>
Notwithstanding, the state has a budding entrepreneurial sector comprising mainly micro, small
and medium enterprises (MSMEs). Whereas the application of basic technology and other
context-specific opportunities have enabled the establishment and operation of a variety of these
enterprises, to a great measure, they still operate within the formal and semi-formal domains. In
order to up our game in meeting the challenges mentioned earlier on, Bayelsa State requires a
multi-dimensional approach that addresses public and private constrains on development and take
full advantage of the immense natural resource endowments and development potentials within
her grip.</p>


<p>
Central amongst other instruments for unlocking Bayelsa State’s development potential and
transforming the afore-mentioned challenges into opportunities is Agriculture. It is therefore,
extremely essential at this point in time to properly position the agricultural sector by way of
taking strategic steps to formalize the entire sub-sector , as well as organize systems , processes
and operations of actors, stakeholders, government and the rural farmers. This involves a range of
activities and processes including identification of relevant and viable areas of agriculture that can
be harnessed using projected success indices as a premise based on experience and success stories. 
</p>

<p>Government has always played a critical role in developing the agricultural sector alongside
intervention programmes from local and international bodies. However, to move forward, there is
need to keep accurate data of agricultural processes, practices and operations. Additionally, there
is need to identify the various value chains in the agriculture sub-sectors. There also has to be a
robust and all-inclusive collaboration amongst all actors: government, private sector, IOCs,
financial institutions, farmers etc, for proper decision making and policy formulation for the
agriculture sector in Bayelsa state for it to thrive in the 21st century. At the subsistence level, this
can be actualized when farmers have substantial knowledge of how and where to find and get
support-financial and otherwise- for them to take care of themselves, supply local markets for
internal agricultural trade and take their produce to other states and beyond. On the international
front, the state must ensure a degree of ownership of some of the initiatives and provide support
and linkages for farmers with the will and might to go international. </p>

<p>Bayelsa state is home to the longest coastline in the West- African sub-region with soil, climate
and ambiences conducive for crops such as rice, cassava, plantain, banana, cane-sugar amongst
others to do well.
</p>

<p>The summit seeks to answer all the frequently asked questions concerning agriculture in the state,
using the Prosperity agenda of the incumbent administration as a vehicle with the theme:
<b style="color:#000">“Transforming Challenges into Opportunities”</b>.
</p>
